<script>
    var gameid = '<?= @$game_details['id'] ?>';
    $(function () {
        var tr = '';
        $.ajax({
            url: base_url + 'Predictions/game_reward',
            data: {'game_id': gameid},
            dataType: 'JSON',
            type: 'POST',
            success: function (res) {
                var tr = '';
                if (!res.length == 0) {
                    $.each(res, function (key, value) {
                        tr += '<tr><td>' + value.description + '</td><td>' + value.price + '</td></tr>';
                    });
                    $('#reward_table').append(tr);
                }
            }
        });
    })
</script>